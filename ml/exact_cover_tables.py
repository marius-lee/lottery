import itertools

def verify_cover(tickets, v, t=4):
    """暴力验证: 对所有 C(v,6) 开奖组合, 检查是否每注都有 ≥t 命中。

    Returns:
        {"total": N, "covered": M, "uncovered": U, "coverage_pct": P}
    """
    uncovered = 0
    all_6 = list(itertools.combinations(range(1, v+1), 6))
    for draw in all_6:
        draw_set = set(draw)
        if not any(len(set(tk) & draw_set) >= t for tk in tickets):
            uncovered += 1
    total = len(all_6)
    return {
        "total": total,
        "covered": total - uncovered,
        "uncovered": uncovered,
        "coverage_pct": round((total - uncovered) / total * 100, 1),
        "guarantee": f"若{v}个号含全部6个开奖号 → {(total-uncovered)/total*100:.1f}% 至少命中{t}红",
    }


