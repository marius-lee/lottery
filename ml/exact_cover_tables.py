def take_top_n(v, t, n, hot_numbers=None):
    """从完整 C(v,6,t) 覆盖表中取前 N 注, 贪心优先选覆盖最多的。

    完整表是确定性的 — 每注的构造位置都是精确设计的。
    直接取前 N 注即可获得最优的渐进覆盖, 无需重新运行贪心。

    Args:
        v: 号码池大小 (12-14, 必须有已知表)
        t: 覆盖强度
        n: 需要的注数
        hot_numbers: 用户选定的 v 个号码, 默认 1..v

    Returns:
        list of [r1..r6] tickets mapped to hot_numbers
    """
    table = FULL_COVERS.get((v, t))
    if table is None:
        return None

    subset = table[:n]

    if hot_numbers and len(hot_numbers) >= v:
        mapping = {i+1: hot_numbers[i] for i in range(v)}
        return [sorted(mapping[x] for x in row) for row in subset]
    return [sorted(row) for row in subset]


